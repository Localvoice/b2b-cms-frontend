export const AuthPermissionConverter: Converter = {
  fromJson(value: any[]): AuthPermission[] {
    return coerceArray(value).reduce((acc, it) => {
      if (AuthPermission[it]) {
        acc.push(AuthPermission[it]);
      }
      return acc;
    }, []);
  },
  toJson(value: AuthPermission[]): string[] {
    return coerceArray(value).reduce((acc, it) => {
      if (AuthPermission[it]) {
        acc.push(AuthPermission[it]);
      }
      return acc;
    }, []);
  },
};
